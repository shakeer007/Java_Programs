public class RelationalOperator {
    
    public static void main(String[] args) {
        int num1 =30;
        int num2 =60;
        System.out.println(num1);
        System.out.println(num1==num2);
        System.out.println(num1<num2);
        System.out.println(num1>num2);
        System.out.println(num1<=num2);
        System.out.println(num1>=num2);

        boolean a =true;
        boolean b =false;
        System.out.println(a&&b);
        System.out.println(a||b);
        System.out.println(!a);
        System.out.println(!b);



    }
}
